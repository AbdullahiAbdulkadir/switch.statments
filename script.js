let roll = 11;

switch (roll) {
  case 1:
    console.log("You rolled a 1");
    break;
  case 2:
    console.log("You rolled a 2");
    break;
  case 3:
    console.log("You rolled a 3");
    break;
  case 4:
    console.log("You rolled a 4");
    break;
  case 5:
    console.log("You rolled a 5");
    break;
    default:
        console.log(roll, "The number is not possible");
}